class typecasting
{
	public static void main(String args[])
	{
		int q=9;
		System.out.println("Before Typecasting:"+q);
		float w=q;
		System.out.println("After Typecasting:"+w);
		

		System.out.println("_______________________________________________________");

		float e=8.9f;
		System.out.println("Before Typecasting:"+e);
		int d=(int)e;
		System.out.println("After Typecasting:"+d);
		
		System.out.println("_______________________________________________________");

		int z=66;
		System.out.println("Before Typecasting:"+z);
		double y=z;
	        System.out.println("After Typecasting:"+y);
		
		System.out.println("_______________________________________________________");

		double o=7.7;
		System.out.println("Before Typecasting:"+o);
		int p=(int)o;
		System.out.println("Before Typecasting:"+p);

	}
}